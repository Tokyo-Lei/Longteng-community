//一级回复表
const mongoose = require('mongoose');
const Schema = mongoose.Schema;
const BaseModel = require('./base_model');
const shortid = require('shortid');
const ReplySchema = new Schema({
    //留言的ID
    _id:{
        type:String,
        default:shortid.generate,
        unique:true
    },
    //留言的内容
    content:{
        type:String,
        require:true
    },
    //留言的人
    author:{
        type:String,
        ref:'User'
    },
    //留言的时间
    create_time:{
        type:Date,
        default:Date.now
    },
    //二级回复的ID
    reply_id:{
        type:String
    },
    //对应的问题
    question_id:{
        type:String,
        ref:'Question'
    },
    //喜欢该留言的人的数组
    likes:{
        type:[String],
        ref:'User'
    },
    //二级回复的数量
    comment_num:{
        type:Number,
        default:0
    }
})
ReplySchema.plugin(BaseModel);
ReplySchema.statics = {
    getRepliesByQuestionId:(question_id,callback)=>{
        Reply.find({'question_id':question_id}).sort({'create_time':1}).populate('author').exec(callback)
    },
    getRepliesByQuestionIdFive:(question_id,callback)=>{
        Reply.find({'question_id':question_id}).sort({'create_time':1}).limit(5).populate('author').exec(callback)
    },
    getRepliesByAuthor:(author_id,callback)=>{
        Reply.find({'author':author_id}).populate('question_id').sort({'create_time':1}).exec(callback);
    }
}
const Reply = mongoose.model('Reply',ReplySchema);
module.exports = Reply
